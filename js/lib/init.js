(function() {
	// Google Web Fonts
    var link = document.createElement('link');
    link.type = "text/css";
    link.rel = "stylesheet";
    link.href = "http://fonts.googleapis.com/css?family=Open+Sans:400,300,600,700";
    document.querySelector("head").appendChild(link);
    // Style 
    var link = document.createElement('link');
    link.type = "text/css";
    link.rel = "stylesheet";
    link.href = "css/style.css";
    document.querySelector("head").appendChild(link);
})();